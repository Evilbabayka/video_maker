def validate_config(config):
    """
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç—å –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏

    Args:
        config (dict): –°–ª–æ–≤–∞—Ä—å —Å –Ω–∞—Å—Ç—Ä–æ–π–∫–∞–º–∏

    Returns:
        tuple: (is_valid, errors) - –≤–∞–ª–∏–¥–Ω–æ—Å—Ç—å –∏ —Å–ø–∏—Å–æ–∫ –æ—à–∏–±–æ–∫
    """
    errors = []

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ä–∞–∑—Ä–µ—à–µ–Ω–∏—è
    if (
        not isinstance(config.get("resolution"), tuple)
        or len(config["resolution"]) != 2
    ):
        errors.append("–†–∞–∑—Ä–µ—à–µ–Ω–∏–µ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –∫–æ—Ä—Ç–µ–∂–µ–º –∏–∑ –¥–≤—É—Ö —á–∏—Å–µ–ª")

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ FPS
    fps = config.get("fps", 0)
    if not isinstance(fps, (int, float)) or fps <= 0 or fps > 120:
        errors.append("FPS –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å —á–∏—Å–ª–æ–º –æ—Ç 1 –¥–æ 120")

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–ª–∏—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ –∫–∞–¥—Ä–∞
    duration = config.get("image_duration", 0)
    if not isinstance(duration, (int, float)) or duration <= 0:
        errors.append("–î–ª–∏—Ç–µ–ª—å–Ω–æ—Å—Ç—å –∫–∞–¥—Ä–∞ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω—ã–º —á–∏—Å–ª–æ–º")

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ä–∞–∑–º–µ—Ä–∞ —à—Ä–∏—Ñ—Ç–∞
    fontsize = config.get("subtitle_fontsize", 0)
    if not isinstance(fontsize, (int, float)) or fontsize <= 0:
        errors.append("–†–∞–∑–º–µ—Ä —à—Ä–∏—Ñ—Ç–∞ –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω—ã–º —á–∏—Å–ª–æ–º")

    return len(errors) == 0, errors


